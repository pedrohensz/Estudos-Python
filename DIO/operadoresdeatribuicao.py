"sobrescrevendo a atribuição"
saldo = 500

print(f"Resposta 1: {saldo}")

saldo = 200

print (f"Resposta 2: {saldo}")

"Atribuição com operações"

"Soma"
saldo += 10
print (f"Soma: {saldo}" )

"Subtração"
saldo -= 5
print (f"Subtração: {saldo}" )

"Divisão"
saldo /= 2
print (f"Divisão: {saldo}" )


"Divisão sem resto"
saldo //= 2
print (f"Divisão sem resto: {saldo}" )

"Multiplicação"
saldo *= 10
print (f"Multiplicação: {saldo}" )

"Exponenciação"
saldo **= 2
print (f"Exponenciação: {saldo}" )