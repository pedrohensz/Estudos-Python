""" 
Tipos de dados 
1. Numéricos
int: números inteiros (ex: 10, -3, 0)

float: números com ponto flutuante (ex: 3.14, -0.5, 2.0)

complex: números complexos (ex: 2 + 3j)

2. Booleano
bool: representa valores lógicos True ou False

3. Texto
str: sequência de caracteres (ex: "Pedro", 'Python')

4. Sequências
list: lista ordenada e mutável (ex: [1, 2, 3], ['a', 'b', 'c'])

tuple: tupla ordenada e imutável (ex: (1, 2, 3))

range: sequência de números inteiros (ex: range(0, 10))

5. Conjuntos
set: coleção não ordenada de elementos únicos (ex: {1, 2, 3})

frozenset: igual ao set, mas imutável

6. Dicionários
dict: pares chave-valor (ex: {'nome': 'Pedro', 'idade': 22})


"""

