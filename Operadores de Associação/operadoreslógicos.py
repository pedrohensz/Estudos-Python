#Operadores Lógicos
#Operador AND (E)
idade = 25
tem_carteira = True

if idade >= 18 and tem_carteira:
    print(True)
#O operador E retorna um valor positivo caso as duas condições lógicas sejam satisfeitas

#Operador OR (OU)
chovendo = False
tem_guarda_chuva = True
if chovendo or tem_guarda_chuva:
    print("Pode sair tranquilo")

#O operador OR retorna um valor positivo caso uma das duas condições lógicas sejam satisfeitas

#Operador NOT (Não)
tem_comida = False
if not tem_comida:
    print("Precisa fazer compras")
else:
    print("Ok")

"""O operador NOT  basicamente inverte o valor, nesse caso tem comida false mas no caso
    o operador if not inverte o valor (para TRUE) e ativa a condição "Precisa fazer compras"""

